'''
1 : 1 (1이 1, 0이 0)
2 : 10 (1이 0, 0이 1)
3 : 101, 100 (1이 1, 0이 1)
4 : 1010, 1001, 1000 (1이 1, 0이 2)
5 : 10100, 10101, 10010, 10001, 10000 (1이 2, 0이 3)
6 : 101000, 101001, 101010, 100101, 100100, 100010, 100000, 100001 (1이 3, 0이 5)
'''
N = int(input())
res = [0]*(N+1)
res[1] = 1
for i in range(2,N+1) :
    res[i] = res[i-1] + res[i-2]
print(res[N])