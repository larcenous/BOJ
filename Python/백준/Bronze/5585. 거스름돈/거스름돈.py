N = 1000 - int(input())
ans = 0
while N/500 >= 1 :
    N -= 500
    ans += 1
while N/100 >= 1 :
    N -= 100
    ans += 1
while N/50 >= 1 :
    N -= 50
    ans += 1
while N/10 >= 1 :
    N -= 10
    ans += 1
while N/5 >= 1 :
    N -= 5
    ans += 1
while N/1 >= 1 :
    N -= 1
    ans += 1
print(ans)